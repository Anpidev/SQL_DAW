SELECT SUM(SALARIO) AS SUMASALARIOS FROM EMPLEADOS;
/*la desviación estándar se calcula como la raíz cuadrada 
de la varianza. La varianza es la media de las diferencias 
al cuadrado entre cada valor y la media del conjunto de datos. 
Al tomar la raíz cuadrada de la varianza, obtenemos
una medida que está en la misma escala que los datos
originales, lo que facilita la interpretación.*/
SELECT SQRT(AVG((SALARIO - (SELECT AVG(SALARIO) FROM EMPLEADOS)) * (SALARIO - (SELECT AVG(SALARIO) FROM EMPLEADOS)))) AS DESVIACION_TIPICA
FROM EMPLEADOS;
