/*CLIENTES QUE HAN HECHO PEDIDOS DE MADRID*/

SELECT *
FROM CLIENTES
INNER JOIN PEDIDOS ON CLIENTES.`CÓDIGO CLIENTE` = PEDIDOS.`CÓDIGO CLIENTE` WHERE POBLACIÓN="MADRID";

/*CLIENTES DE MADRID QUE HAN HECHO PEDIDOS Y QUE NO , DE MADRID*/

SELECT *
FROM CLIENTES
LEFT JOIN PEDIDOS ON CLIENTES.`CÓDIGO CLIENTE` = PEDIDOS.`CÓDIGO CLIENTE` WHERE POBLACIÓN="MADRID";


